<h2>Coder Dojo Rotterdam #1</h2>

<p>Ben jij tussen de 7 en 18 jaar en wil je meer weten over het bouwen van websites? Registreer je dan nu om deel te nemen aan deze Coder Dojo!
Deze eerste Dojo zal voornamelijk een inleiding zijn voor web development. We beginnen deze dojo met de basis over de werking van het internet en hoe websites (technisch) in elkaar zitten. We zullen dit doen in de vorm van een presentatie, een workshop en een hackaton.</p>

<h3>PROGRAMMMA</h3>
<p>12:30 Inloop<br/>
13:00 Inleiding Coder Dojo Rotterdam<br/>
13:15 Christian Vermeulen | Werking Internet<br/>
13:45 Christian Vermeulen | Inleiding HTML / CSS<br/>
14:15 Workshop Front-end<br/>
15:00 Hackaton<br/>
16:30 Kennisdelen<br/>
17:00 Afsluiting</p>

<h3>WAT MOET IK MEENEMEN?</h3>
<ul>
<li>Een eigen laptop</li>
