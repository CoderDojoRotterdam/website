<h2>Welkom bij Coderdojo Rotterdam</h2>
<p>CoderDojo Rotterdam verzorgt gratis lessen / workshops om kinderen van 7 - 18 jaar te leren programmeren. Wij organiseren regelmatig evenementen waar kinderen, met hun ouders, kunnen samenkomen om te werken aan websites, games, apps en software. Het hele project wordt door vrijwilligers opgezet!</p>
